function solution(elements) {
  var answer = 0;
  const set = new Set();
  const N = elements.length;

  for (let i = 0; i < N; i++) {
    let end = i;
    let sum = 0;

    while (1) {
      sum += elements[end];
      if (!set.has(sum)) set.add(sum);

      //   0ì¼ë•Œ ë¬¸ì œë‹¤.
      //   if (end === i - 1) break;
      if (end === (i + N - 1) % N) break;

      end = (end + 1) % N;
    }
  }

  answer = set.size;

  return answer;
}
