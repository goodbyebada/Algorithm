const path = process.platform === "linux" ? "/dev/stdin" : "./input.txt";
const [T, ...stairs] = require("fs")
  .readFileSync(path)
  .toString()
  .trim()
  .split("\n")
  .map(Number);

const dp = new Array(T).fill(0);

// (idx+1) ê³„ë‹¨
dp[0] = stairs[0];
dp[1] = stairs[0] + stairs[1];
