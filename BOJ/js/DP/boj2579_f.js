// 2.  n-2 ë²ˆì§¸ ê³„ë‹¨ì„ ë°ŸëŠ” ê²½ìš° (+2)
// => n-2 ê³„ë‹¨ + n ê³„ë‹¨
// dp[n-2] + stairs[n]

// dp[i] = Math.max(1ì—°ì†, 2ì—°ì†) ì—…ë°ì´íŠ¸

for (let i = 3; i < T; i++) {
  dp[i] = stairs[i] + Math.max(dp[i - 3] + stairs[i - 1], dp[i - 2]);
}

